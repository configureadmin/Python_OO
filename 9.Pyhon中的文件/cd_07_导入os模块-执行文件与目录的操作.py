### 在Python开发中,要实现对文件及目录的管理,需要导入os模块
# 文件或目录的操作,都支持 相对路径与绝对路径
### os模块中的文件操作方法
# rename: 重命名 os.rename("源文件名","目标文件名")
# remove: 删除文件 os.remove("要删除的文件名")

### os模块中的目录操作方法
# listdir: 目录列表 os.listdir("目录名")
# mkdir: 创建目录 os.mkdir("目录名")
# rmdir: 删除目录 os.redir("目录名")
# getcwd: 获取当前目录 os.getcwd()
# chdir: 修改工作目录 os.chdir("目标目录")
# path.isdir: 判断是否是目录 os.path.isdir("目录路径")





